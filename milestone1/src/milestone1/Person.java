package milestone1;

public class Person {
	private String name;
	private int age;
	
	public Person(String name, int age) {
		this.setAge(31);
		this.setName("Rolando");
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}

}
